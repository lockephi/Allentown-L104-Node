def make_fade_envelope(n_samples: int, fade_samples: int) -> np.ndarray:
    """Create a fade-in / fade-out cosine envelope (no clicks)."""
    env = np.ones(n_samples, dtype=np.float64)
    if fade_samples > 0 and fade_samples * 2 < n_samples:
        # Cosine fade-in: 0 → 1
        fade_in = 0.5 * (1.0 - np.cos(np.linspace(0, np.pi, fade_samples)))
        # Cosine fade-out: 1 → 0
        fade_out = 0.5 * (1.0 + np.cos(np.linspace(0, np.pi, fade_samples)))
        env[:fade_samples] = fade_in
        env[-fade_samples:] = fade_out
    return env


def write_wav(filename: str, signal: np.ndarray, sample_rate: int,
              bit_depth: int = 24, channels: int = 1) -> float:
    """
    Encode and write a float64 signal as 16-bit or 24-bit PCM WAV.
    For stereo, signal shape is (n_samples, 2).
    Returns file size in MB.
    """
    if bit_depth == 24:
        scale = 8388607  # 2^23 - 1
        samp_width = 3
    else:
        scale = 32767    # 2^15 - 1
        samp_width = 2

    if channels == 2 and signal.ndim == 2:
        # Interleave L/R for stereo
        interleaved = signal.flatten(order='C')  # [L0, R0, L1, R1, ...]
    else:
        interleaved = signal.ravel()

    if bit_depth == 24:
        samples_int = (interleaved * scale).astype(np.int32)
        raw = samples_int.view(np.uint8).reshape(-1, 4)[:, :3].flatten().tobytes()
    else:
        samples_int = (interleaved * scale).astype(np.int16)
        raw = samples_int.tobytes()

    with wave.open(filename, 'w') as wav:
        wav.setnchannels(channels)
        wav.setsampwidth(samp_width)
        wav.setframerate(sample_rate)
        wav.writeframes(raw)
    return len(raw) / (1024 * 1024)


def normalize_signal(signal: np.ndarray, amplitude: float) -> np.ndarray:
    """Peak-normalize a signal to the given amplitude."""
    peak = np.max(np.abs(signal))
    if peak > 1e-30:
        signal = signal / peak * amplitude
    return signal


# ── Performance Constants ────────────────────────────────────────────────────
_SYNTH_CHUNK = 1_000_000  # Samples per chunk for cache-friendly vectorization
# 13 partials × 1M samples × 8 bytes = 104 MB (fits in L3 cache)
# Multi-core: each thread gets its own chunk range, writes to disjoint memory
_MULTICORE_THRESHOLD = 4_000_000  # Use multi-core for >4M samples (~42s at 96kHz)


def _process_chunk(args):
    """Process a single chunk of additive synthesis (for ThreadPoolExecutor)."""
    c0, c1, freqs, phases_base, weights_arr, t, phase_mod = args
    two_pi = 2.0 * np.pi
    f_col = freqs[:, np.newaxis]
    p_col = phases_base[:, np.newaxis]
    w_col = weights_arr[:, np.newaxis]
    t_c = t[c0:c1]
    phi = two_pi * f_col * t_c[np.newaxis, :] + p_col
    if phase_mod is not None:
        if callable(phase_mod):
            phi += phase_mod(c0, c1)
    out = np.sum(w_col * np.sin(phi), axis=0)
    return c0, c1, out


def _vectorized_additive(freqs: np.ndarray, phases_base: np.ndarray,
                          weights_arr: np.ndarray, t: np.ndarray,
                          n_samples: int, phase_mod: object = None,
                          chunk_size: int = _SYNTH_CHUNK) -> np.ndarray:
    """High-performance vectorized additive synthesis — CPU multi-core + Metal GPU.

    v8.2 PERFORMANCE UPGRADE:
      - Metal GPU path: offloads simple (no phase_mod) synthesis to GPU via
        metal_additive_synthesis() for 10-50× throughput on Apple Silicon.
      - Multi-core path: for >4M samples with phase_mod, distributes chunks
        across CPU threads for ~2-4× speedup on multi-core Macs.
      - Single-core path: cache-friendly L3-resident chunking (original).

    Parameters
    ----------
    freqs : ndarray (N,) — frequencies
    phases_base : ndarray (N,) — base phases
    weights_arr : ndarray (N,) — amplitudes
    t : ndarray (n_samples,) — time array
    n_samples : int — total samples
    phase_mod : None | ndarray (N, n_samples) | callable(c0, c1) -> (N, chunk)
    chunk_size : int — samples per chunk

    Returns
    -------
    signal : ndarray (n_samples,)
    """
    n_partials = len(freqs)

    # ── Path 1: Metal GPU (no phase_mod, simple additive) ────────────────
    if phase_mod is None and _METAL_AVAILABLE and n_samples >= 100_000:
        try:
            return metal_additive_synthesis(freqs, phases_base, weights_arr,
                                            n_samples, int(t[-1] * n_samples / max(t[-1], 1e-30))
                                            if len(t) > 0 else 96000)
        except Exception:
            pass  # Fall through to CPU

    signal = np.zeros(n_samples, dtype=np.float64)
    two_pi = 2.0 * np.pi
    f_col = freqs[:, np.newaxis]
    p_col = phases_base[:, np.newaxis]
    w_col = weights_arr[:, np.newaxis]

    # ── Path 2: Multi-core CPU (large arrays with phase_mod) ─────────────
    if n_samples >= _MULTICORE_THRESHOLD and _SYNTHESIS_WORKERS >= 2:
        # Build chunk list
        chunks = []
        for c0 in range(0, n_samples, chunk_size):
            c1 = min(c0 + chunk_size, n_samples)
            chunks.append((c0, c1, freqs, phases_base, weights_arr, t, phase_mod))

        if len(chunks) > 1:
            # Distribute chunks across threads (NumPy releases GIL for math ops)
            with ThreadPoolExecutor(max_workers=_SYNTHESIS_WORKERS) as pool:
                futures = [pool.submit(_process_chunk, ch) for ch in chunks]
                for future in as_completed(futures):
                    c0, c1, out = future.result()
                    signal[c0:c1] = out
            return signal

    # ── Path 3: Single-core cache-friendly chunking (fallback) ───────────
    for c0 in range(0, n_samples, chunk_size):
        c1 = min(c0 + chunk_size, n_samples)
        t_c = t[c0:c1]
        phi = two_pi * f_col * t_c[np.newaxis, :] + p_col
        if phase_mod is not None:
            if callable(phase_mod):
                phi += phase_mod(c0, c1)
            else:
                phi += phase_mod[:, c0:c1]
        signal[c0:c1] = np.sum(w_col * np.sin(phi), axis=0)
    return signal


