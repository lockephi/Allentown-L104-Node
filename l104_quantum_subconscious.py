    def _load_state(self):
        """Load persisted subconscious state (v2.0: restores autonomous state)."""
        try:
            if self._state_path.exists():
                data = json.loads(self._state_path.read_text())
                self._cycles_completed = data.get("cycles_completed", 0)
                self._total_thoughts_generated = data.get("total_thoughts_generated", 0)
                self._total_insights_crystallized = data.get("total_insights_crystallized", 0)
                self._total_precog_seeds = data.get("total_precog_seeds", 0)
                self._total_elapsed_ms = data.get("total_elapsed_ms", 0.0)
                saved_counts = data.get("stream_counts", {})
                for k, v in saved_counts.items():
                    if k in self._stream_counts:
                        self._stream_counts[k] = v

                # v2.0: Restore autonomous state
                autonomous = data.get("autonomous", {})
                if autonomous:
                    saved_interval = autonomous.get("current_interval")
                    if saved_interval and ADAPTIVE_INTERVAL_MIN_S <= saved_interval <= ADAPTIVE_INTERVAL_MAX_S:
                        self._current_interval = saved_interval
                    self._total_proactive_pushes = autonomous.get(
                        "total_proactive_pushes", 0)
                    self._total_emergent_spawns = autonomous.get(
                        "total_emergent_spawns", 0)
                    self._total_stream_restarts = autonomous.get(
                        "total_stream_restarts", 0)
                    self._total_evolution_generations = autonomous.get(
                        "evolution_generations", 0)
                    self._interval_adjustments = autonomous.get(
                        "interval_adjustments", 0)
                    # Restore stream weights
                    saved_weights = autonomous.get("stream_weights", {})
                    for name, weight in saved_weights.items():
                        self._stream_weighter._ensure_stream(name)
                        self._stream_weighter._weights[name] = max(
                            STREAM_MIN_WEIGHT, min(STREAM_MAX_WEIGHT, weight))
                    # Restore crystallizer threshold
                    saved_thresh = autonomous.get("crystallizer_threshold")
                    if saved_thresh:
                        self._adaptive_crystallizer._threshold = max(
                            CRYSTALLIZATION_MIN_THRESHOLD,
                            min(CRYSTALLIZATION_MAX_THRESHOLD, saved_thresh))
        except Exception:
            pass

