class AdaptiveStreamWeighter:
    """Dynamically adjusts stream priority weights using φ-gradient descent.

    High-quality streams (high coherence, frequent insights) get boosted.
    Poor streams get deprioritized but never fully silenced.

    Weight update rule:
      w_new = w_old + η × (quality - mean_quality) × φ^(-1)
      Clamped to [STREAM_MIN_WEIGHT, STREAM_MAX_WEIGHT]
    """

    def __init__(self):
        self._weights: Dict[str, float] = {}
        self._quality_history: Dict[str, List[float]] = {}

    def _ensure_stream(self, stream_name: str):
        if stream_name not in self._weights:
            self._weights[stream_name] = 1.0  # Equal initial weight
            self._quality_history[stream_name] = []

    def record_quality(self, stream_name: str, coherence: float,
                       alignment: float, produced_insight: bool):
        """Record a quality observation for weight update."""
        self._ensure_stream(stream_name)
        quality = coherence * alignment
        if produced_insight:
            quality *= GROVER_AMPLIFICATION  # Boost for insight production
        self._quality_history[stream_name].append(quality)
        # Keep window bounded
        if len(self._quality_history[stream_name]) > STREAM_HEALTH_WINDOW:
            self._quality_history[stream_name] = (
                self._quality_history[stream_name][-STREAM_HEALTH_WINDOW:])

    def update_weights(self):
        """Perform φ-gradient weight update across all streams."""
        if not self._weights:
            return

        # Compute mean quality across all streams
        all_qualities = []
        stream_means = {}
        for name, history in self._quality_history.items():
            if history:
                mean_q = sum(history) / len(history)
                stream_means[name] = mean_q
                all_qualities.append(mean_q)

        if not all_qualities:
            return

        global_mean = sum(all_qualities) / len(all_qualities)

        # φ-gradient update
        for name in self._weights:
            stream_q = stream_means.get(name, 0)
            gradient = (stream_q - global_mean) * PHI_CONJUGATE
            self._weights[name] += STREAM_WEIGHT_LEARN_RATE * gradient
            # Clamp
            self._weights[name] = max(STREAM_MIN_WEIGHT,
                                      min(STREAM_MAX_WEIGHT, self._weights[name]))

    def get_weight(self, stream_name: str) -> float:
        """Get the current priority weight for a stream."""
        self._ensure_stream(stream_name)
        return self._weights[stream_name]

    def get_all_weights(self) -> Dict[str, float]:
        """Get all stream weights."""
        return dict(self._weights)

    def should_fire(self, stream_name: str) -> bool:
        """Determine if a stream should fire this cycle based on weight.

        Weight > 1.0: always fires, may fire multiple times
        Weight 0.5-1.0: fires with probability = weight
        Weight < 0.5: fires occasionally
        """
        w = self.get_weight(stream_name)
        if w >= 1.0:
            return True
        return random.random() < w

    def get_extra_fires(self, stream_name: str) -> int:
        """Get number of extra fires for high-weight streams."""
        w = self.get_weight(stream_name)
        if w <= 1.0:
            return 0
        # Extra fires proportional to weight above 1.0
        return int(w - 1.0)


